    private static void updateTable() {
        try {

            String[][] data = Manage_People.getDataPeople("");

            m = new DefaultTableModel(data, columnsNames);

            tbl_Clients.setModel(m);

        } catch (SQLException ex) {
            Logger.getLogger(If_ManageClients.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    //Search a client
    private static void searchClients(String cedula) {
        try {
            String[][] data = Manage_People.getDataPeople(cedula);
            m = new DefaultTableModel(data, columnsNames);
            tbl_Clients.setModel(m);
        } catch (SQLException ex) {
            Logger.getLogger(If_ManageClients.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

