    private void btn_EditClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditClientActionPerformed

        if (tbl_Clients.getSelectedRow() != -1) {
            If_NewPerson men = new If_NewPerson();
            String cedula = m.getValueAt(tbl_Clients.getSelectedRow(), 0).toString(), name = m.getValueAt(tbl_Clients.getSelectedRow(), 1).toString();
            String surname1 = m.getValueAt(tbl_Clients.getSelectedRow(), 2).toString(), surname2 = m.getValueAt(tbl_Clients.getSelectedRow(), 3).toString();
            String dir = m.getValueAt(tbl_Clients.getSelectedRow(), 4).toString(), tlf = m.getValueAt(tbl_Clients.getSelectedRow(), 5).toString();
            String email = m.getValueAt(tbl_Clients.getSelectedRow(), 6).toString();

         
            men.editPerson(cedula, name, surname1, surname2, dir, tlf, email);

            men.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione una fila");
        }


    }//GEN-LAST:event_btn_EditClientActionPerformed

    private void btn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteActionPerformed

        if (tbl_Clients.getSelectedRow() != -1) {
            String cedula = m.getValueAt(tbl_Clients.getSelectedRow(), 0).toString();
            Manage_People.deletePerson(cedula);
            JOptionPane.showMessageDialog(rootPane, "Cliente Eliminado");
            updateTable();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione una fila");
        }
        
        updateTable();
    }//GEN-LAST:event_btn_DeleteActionPerformed

