    //Update tbl_Cashiers
    private static void updateTable() {
        try {
            
            String[][] data = Manage_Products.getDataProducts("");
            
            m = new DefaultTableModel(data, columnsNames);
            
            tbl_Products.setModel(m);
            
        } catch (SQLException ex) {
            Logger.getLogger(If_ManageProducts.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    //Search a Product through his name
    private static void searchProduct(String nameProduct) {
        
        try {
            
            String[][] data = Manage_Products.getDataProducts(nameProduct);
            m = new DefaultTableModel(data, columnsNames);
            tbl_Products.setModel(m);
            
        } catch (SQLException ex) {
            Logger.getLogger(If_ManageProducts.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
