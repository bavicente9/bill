    private void btn_EditClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditClientActionPerformed
        
        if (tbl_Products.getSelectedRow() != -1) {
            //Send actual select data's person to If_NewPerson
            If_NewProduct men = new If_NewProduct();
            String idProduct = m.getValueAt(tbl_Products.getSelectedRow(), 0).toString(), name = m.getValueAt(tbl_Products.getSelectedRow(), 1).toString();
            String priceU = m.getValueAt(tbl_Products.getSelectedRow(), 2).toString();
            
            men.editProduct(idProduct, name, priceU);
            men.setVisible(true);
            
        } else {
            
            JOptionPane.showMessageDialog(rootPane, "Selecione una fila");
        }
        

    }//GEN-LAST:event_btn_EditClientActionPerformed

