    private void btn_detailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_detailsActionPerformed

        //opens  If_ViewInvoiceDetails and sends the requested nInvoice
        int rowSelected = tbl_Invoices.getSelectedRow();
        if (rowSelected > -1) {
            try {
                int nInvoice = Integer.parseInt(m.getValueAt(rowSelected,0).toString());
                If_ViewInvoiceDetails men = new If_ViewInvoiceDetails();
                men.showInvoiceDetails(nInvoice);
                
                men.setVisible(true);
                this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(If_ManageInvoices.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }

    }//GEN-LAST:event_btn_detailsActionPerformed

