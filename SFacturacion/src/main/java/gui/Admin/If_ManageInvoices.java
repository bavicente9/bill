    //Update tbl_Cashiers
    private static void updateTable() {
        try {

            String[][] data = Manage_Invoices.getDataInvoices("");

            m = new DefaultTableModel(data, columnsNames);

            tbl_Invoices.setModel(m);

        } catch (SQLException ex) {
            Logger.getLogger(If_ManageInvoices.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    //Search a Invoice through his name
    private static void searchInvoice(String idInvoice) {

        try {

            String[][] data = Manage_Invoices.getDataInvoices(idInvoice);
            m = new DefaultTableModel(data, columnsNames);
            tbl_Invoices.setModel(m);

        } catch (SQLException ex) {
            Logger.getLogger(If_ManageInvoices.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

