    private void btn_DeleteProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteProductActionPerformed

        //delete a product from the table and restore the stock
        int nRows = modelTable.getRowCount();
        int rowSelected = tbl_ProductsAdded.getSelectedRow();
        //check if is selected a product
        if (rowSelected <= (nRows - 3)) {
            int id = Integer.parseInt(modelTable.getValueAt(rowSelected, 0).toString());
            int stock = Integer.parseInt(modelTable.getValueAt(rowSelected, 3).toString());

            try {
                //restore the stock
                back.Manage_Products.updateStock(id, Manage_Products.getStock(id) + stock);

                //delete the product from the table and recalculate prices
                modelTable.removeRow(rowSelected);

                Cashier_InvoiceManager.calculateTotalInTable(modelTable);
                tbl_ProductsAdded.setModel(modelTable);
            } catch (Exception e) {
                System.out.println(e);
            }
        }

    }//GEN-LAST:event_btn_DeleteProductActionPerformed

