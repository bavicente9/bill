    //search client
    private void searchClient() {
        String cedula = txt_Cedula.getText().toLowerCase();
        //Check if the person exist
        if (cedula.length() == 10 && Manage_People.existPerson(cedula)) {
            try {
                //save data from a person to a String[] and then put into txt boxes
                //Order from data  = cedula, name, surname1, surname2, dir, tlf, email

                String[][] dataPerson = Manage_People.getDataPeople(cedula);
                String name, surname1, surname2, dir, tlf, email;
                name = dataPerson[0][1];
                surname1 = dataPerson[0][2];
                surname2 = dataPerson[0][3];
                dir = dataPerson[0][4];
                tlf = dataPerson[0][5];
                email = dataPerson[0][6];
                txt_Name.setText(name);
                txt_Dir.setText(dir);
                txt_Email.setText(email);
                txt_Surname1.setText(surname1);
                txt_Surname2.setText(surname2);
                txt_Tlf.setText(tlf);

                lbl_ErrorCedula.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(If_CashierInvoices.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (!Manage_People.existPerson(cedula)) {
            If_NewPerson men = new If_NewPerson();
 
            men.setVisible(true);
        }
    }

    private void prepareProductsPanel() {
        try {
            //This funtion prepare the combo box with all products and set the model to product's table added.
            //////////////Prepare combo box//////////////
            String products[][] = back.Manage_Products.getDataProducts("");
            String[] productsToCb = new String[products.length];
            //convert each row and column from products[][] to one row in productsToCb[]

            for (int i = 0; i < productsToCb.length; i++) {
                //format
               // productsToCb[i] = products[i][0] + "|" + products[i][1] + "|" + products[i][2] + "$";
              
                productsToCb[i] = String.format("%-5s | %-40s | %6s", products[i][0], products[i][1] , products[i][2]+"$");
            }
            modelCBox = new DefaultComboBoxModel(productsToCb);
            cb_Products.setModel(modelCBox);

            ////////////// Prepare table  //////////////
            String[] columnNames = {"Id", "Nombre", "P/u", "Cantidad", "Precio Total"};
            modelTable = new DefaultTableModel(columnNames, 0);

            /// add 3 last rows for subtotal , iva and total
            String[] voidLines = {"", "", "", "", ""};
            modelTable.addRow(voidLines);
            modelTable.addRow(voidLines);
            modelTable.addRow(voidLines);

            back.Cashier_InvoiceManager.calculateTotalInTable(modelTable);

            tbl_ProductsAdded.setModel(modelTable);

        } catch (SQLException ex) {
            Logger.getLogger(If_CashierInvoices.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void eliminateProductFromCbox(String product) {
        modelCBox.removeElement(product);
        cb_Products.setModel(modelCBox);
    }

